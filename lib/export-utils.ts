    const coverWs = XLSX.utils.aoa_to_sheet(coverData);

    // Style the cover sheet
    const coverRange = XLSX.utils.decode_range(coverWs["!ref"] || "A1");

    // Set column widths
    coverWs["!cols"] = [{ wch: 25 }, { wch: 35 }];

